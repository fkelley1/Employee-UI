//TODO Pass employees list to a function to generate the html to avoid repeated code in getAllEmployeeData and search
function getAllEmployeeData() {
	let employees = getEmployees();
	let employeeList = [];
	let id = 1;
	for (let employee of employees) {
		employeeList.push(`<div class="employee-card" id="employee_id_${id}" onClick={showEmployee(this)} tabindex="0"><div>Employee Name: ${employee.name.firstName} ${employee.name.lastName}</div><div>Phone: ${employee.phone}</div><div> Bio: ${employee.bio} </div></div>`);
		id += 1;
	}
	return employeeList.join('');
}
