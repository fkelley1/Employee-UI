function search() {
	let employees = getEmployees();
	let searchTerm = document.getElementById("txtSearch").value;
	let searchEmployeeList = [];
	let id = 1;
	for (let employee of employees) {
		let fullName = employee.name.firstName + " " + employee.name.lastName;
		//case sensitive search
		if (fullName.includes(searchTerm)) {
			searchEmployeeList.push(`<div class="employee-card" id="employee_id_${id}" onClick={showEmployee(this)} tabindex="0"><div>Employee Name: ${employee.name.firstName} ${employee.name.lastName}</div><div>Phone: ${employee.phone}</div><div> Bio: ${employee.bio} </div></div>`);
			id += 1;
		}
	}
	//no return, just set the list generated from search term
	document.getElementById("employee-list").innerHTML = searchEmployeeList.join('');
}

